
#ifndef _ERRORCODES__H
#define _ERRORCODES__H

#define ERROR_SUCCESS              0x00000000 // 0
#define ERROR_FILE_NOT_FOUND       0x00000002 // 2
#define ERROR_INVALID_HANDLE       0x00000006 // 6
#define ERROR_NOT_ENOUGH_MEMORY    0x00000008 // 8
#define ERROR_BAD_ENVIRONMENT      0x0000000A // 10
#define ERROR_CALL_NOT_IMPLEMENTED 0x00000078 // 120
#define ERROR_DISKCORUPTION        0x00000100
#define ERROR_POINTER              0x00000200
#define ERROR_DISKFULL             0x00000400
#define ERROR_INVALIDDIRNAME       0x00000800
#define ERROR_NAME_EXISTS          0x00001000
#define ERROR_INVALID_SIZE         0x00002000
#define ERROR_UNKNOWN_COMMAND      0x00004000
#define ERROR_EOF                  0x00008000
#define ERROR_HEAP_CORRUPTION      0x00010000
#define ERROR_MAX_CAPACITY         0x00020000
#define ERROR_CONTINUE_SEARCH      0x00040000
#define ERROR_NOT_IMPLEMENTED      0x00080000
#define ERROR_DIRECTORY_NOT_EMPTY  0x00100000

#define HFAILED(t)  t != ERROR_SUCCESS
#define HSUCCEED(t) t == ERROR_SUCCESS

#endif
